package qdg.bits;

import qdg.api.bits.HasId;

public abstract class AbstractIdEntity implements HasId {
	
	protected int id;
	
	protected AbstractIdEntity(int id) {
		this.id = id;
	}

	@Override
	public int getId() {
		return id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AbstractIdEntity other = (AbstractIdEntity) obj;
		if (id != other.id)
			return false;
		return true;
	}
}